//
// 【知识点 / 后端类比】
//   if / else if / else，和后端完全一样。&& 与、|| 或、! 非。
//
//   【注意】含 document 等 DOM API，需在浏览器/有对应 HTML 元素时运行，直接用 node 会报 document is not defined。
//
if (time<10)  // if 条件分支
{
document.write("<b>早上好</b>");  // 直接写入文档流（已过时，页面加载后调用会覆盖整页）
}
else if (time>=10 && time<20)  // if 条件分支
{
document.write("<b>今天好</b>");  // 直接写入文档流（已过时，页面加载后调用会覆盖整页）
}
else
{
document.write("<b>晚上好!</b>");  // 直接写入文档流（已过时，页面加载后调用会覆盖整页）
}
