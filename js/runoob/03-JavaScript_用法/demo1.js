// ============================================================
// 【第 03 节 · JavaScript 用法】demo1：在 HTML 中嵌入 JS 的最简形式
// ============================================================
//
// 【是什么】<script> 标签是 HTML 里写 JS 代码的“容器”。
//           浏览器解析到 <script> 时，会停下 HTML 解析，先执行里面的 JS。
//
// 【后端类比】把 <script> 想象成模板引擎里的代码块（如 PHP 的 <?php ?>、
//            JSP 的 <% %>、Go html/template 的 {{ }}），区别是它运行在
//            【浏览器端】而不是服务器端。
//
// 【alert() 是什么】浏览器内置函数，弹出一个【模态警告框】（会阻塞，直到用户点确定）。
//                  常用于调试/提示，正式项目里几乎不用（用户体验差）。
//
// 【注意】这个文件内容是“HTML 片段”，扩展名虽是 .js，但因为有 <script> 标签，
//        实际应放在 .html 文件里才能运行。这里只是 runoob 用它演示代码结构。
//        单独运行：把这三行保存进一个 .html 文件，用浏览器打开即可看到弹窗。

<script>
alert("我的第一个 JavaScript");  // 弹窗显示这段文字
</script>
