// 【知识点 / 后端类比】
//   三种弹窗：alert(提示)、confirm(确认返回布尔)、prompt(输入返回字符串)。都阻塞。
//   后端类比：浏览器原生的“模态对话框”，正式项目多用自定义 UI 替代。
//
//   【运行方式】可在浏览器控制台运行；纯逻辑代码也可用 node 运行。
//
var r=confirm("按下按钮");  // 确认弹窗，返回 true/false
if (r==true)  // if 条件分支
{
    x="你按下了\"确定\"按钮!";
}
else
{
    x="你按下了\"取消\"按钮!";
}
