// 【知识点 / 后端类比】
//   用 var/let/const 声明变量，JS 是【弱类型/动态类型】——同一变量可存不同类型。
//   后端类比(重点坑)：JS 变量【没有类型声明】，不像 Java int x=1; 而是 var x=1; 之后还能 x='a'。
//   var 只有函数作用域(无块作用域)；let/const(ES6) 才有块作用域。推荐用 let/const。
//
//   【运行方式】可在浏览器控制台运行；纯逻辑代码也可用 node 运行。
//
var x=5;  // var 声明（函数作用域、会提升，推荐改用 let/const）
var y=6;  // var 声明（函数作用域、会提升，推荐改用 let/const）
var z=x+y;  // var 声明（函数作用域、会提升，推荐改用 let/const）
